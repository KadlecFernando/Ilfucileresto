    public void cargarComboMesas() {
        cboListaMesas.removeAllItems();

        List<Mesa> mesas = mD.listarMesas(false);

        cboListaMesas.addItem(null);

        for (Mesa mesa : mesas) {
            cboListaMesas.addItem(mesa);
        }

    }

    public void cargarComboEmp() {
        cboEmp.removeAllItems();

        List<Empleado> empleados = eD.listarEmpleados();

        cboEmp.addItem(null);

        for (Empleado empleado : empleados) {
            cboEmp.addItem(empleado);
        }

    }

